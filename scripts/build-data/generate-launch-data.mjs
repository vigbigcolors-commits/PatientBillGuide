/**
 * Generate full CMS MPFS (RVU26B) + nationwide ZIP + NCCI practitioner PTP (Q3 2026).
 * Run: npm run data:generate
 */

import { createGzip } from 'node:zlib';
import { existsSync, mkdirSync, writeFileSync, createWriteStream, readFileSync } from 'node:fs';
import { pipeline } from 'node:stream/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { Readable } from 'node:stream';
import { CACHE_DIR } from './cms-sources.mjs';
import { cachePaths, parseGpci, parsePprrvu, parseZipLocality } from './lib/cms-parse.mjs';
import { ncciCacheDir, parsePractitionerNcci } from './lib/ncci-parse.mjs';
import { fetchCmsSources } from './fetch-cms.mjs';
import { NCCI_PTP_PAIRS } from './ncci-seed.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const dataDir = join(__dirname, '../../public/data');
const generatedDir = join(__dirname, '../../src/data/generated');

async function writeGzipJson(dest, data) {
  await pipeline(
    Readable.from(JSON.stringify(data)),
    createGzip({ level: 9 }),
    createWriteStream(dest),
  );
}

async function writePricingGzipAssets(dataDir, mpfs, zipMap, codeList) {
  const cptIndex = {
    version: mpfs.version,
    codes: codeList.map((c) => [c, mpfs.codes[c].description_short]),
  };

  await Promise.all([
    writeGzipJson(join(dataDir, 'mpfs-2026.json.gz'), mpfs),
    writeGzipJson(join(dataDir, 'zip-to-locality.json.gz'), zipMap),
    writeGzipJson(join(dataDir, 'cpt-index.json.gz'), cptIndex),
  ]);

  await writeMpfsChunks(dataDir, mpfs);

  return cptIndex;
}

async function writeMpfsChunks(dataDir, mpfs) {
  const chunksDir = join(dataDir, 'mpfs-chunks');
  mkdirSync(chunksDir, { recursive: true });

  const meta = {
    version: mpfs.version,
    source: mpfs.source,
    conversion_factor: mpfs.conversion_factor,
    localities: mpfs.localities,
  };
  await writeGzipJson(join(dataDir, 'mpfs-meta.json.gz'), meta);

  const byPrefix = {};
  for (const [code, record] of Object.entries(mpfs.codes)) {
    const prefix = code[0] ?? '0';
    if (!byPrefix[prefix]) byPrefix[prefix] = {};
    byPrefix[prefix][code] = record;
  }

  for (const [prefix, codes] of Object.entries(byPrefix)) {
    await writeGzipJson(join(chunksDir, `${prefix}.json.gz`), {
      version: mpfs.version,
      prefix,
      codes,
    });
  }
}

function writeManifest(dataDir, codeList, zipMap, ncciParsed, pairCount, prefixCount) {
  const manifest = {
    mpfs: {
      version: '2026',
      mode: 'chunked',
      metaUrl: '/data/mpfs-meta.json.gz',
      chunkUrl: '/data/mpfs-chunks/{prefix}.json.gz',
      url: '/data/mpfs-2026.json.gz',
      codeCount: codeList.length,
    },
    zipLocality: {
      version: '2026',
      url: '/data/zip-to-locality.json.gz',
      zipCount: Object.keys(zipMap).length,
    },
    cptIndex: {
      version: '2026',
      url: '/data/cpt-index.json.gz',
      codeCount: codeList.length,
    },
    ncci: {
      version: ncciParsed?.version ?? '2026Q3',
      mode: 'chunked',
      chunkUrl: '/data/ncci-chunks/{prefix}.json.gz',
      pairCount,
      format: 'compact-v1',
    },
    updatedAt: new Date().toISOString().slice(0, 10),
  };
  writeFileSync(join(dataDir, 'manifest.json'), JSON.stringify(manifest, null, 2) + '\n');
  return manifest;
}

async function ensureGzipFromExisting(dataDir, generatedDir, codesPath) {
  const mpfsPath = join(dataDir, 'mpfs-2026.json');
  const zipPath = join(dataDir, 'zip-to-locality.json');
  const mpfsGzPath = join(dataDir, 'mpfs-2026.json.gz');
  const cptIndexPath = join(dataDir, 'cpt-index.json.gz');

  if (!existsSync(mpfsPath) || !existsSync(zipPath)) return false;
  if (existsSync(mpfsGzPath) && existsSync(cptIndexPath)) return false;

  console.log('Generating gzip pricing assets from existing JSON…');
  const mpfs = JSON.parse(readFileSync(mpfsPath, 'utf8'));
  const zipMap = JSON.parse(readFileSync(zipPath, 'utf8'));
  const codeList = Object.keys(mpfs.codes).sort();

  await writePricingGzipAssets(dataDir, mpfs, zipMap, codeList);

  let pairCount = 0;
  let prefixCount = 0;
  try {
    const manifest = JSON.parse(readFileSync(join(dataDir, 'manifest.json'), 'utf8'));
    pairCount = manifest.ncci?.pairCount ?? 0;
    prefixCount = 36;
  } catch {
    /* manifest may be missing on first partial run */
  }

  writeManifest(dataDir, codeList, zipMap, { version: '2026Q3' }, pairCount, prefixCount);

  if (!existsSync(codesPath)) {
    mkdirSync(generatedDir, { recursive: true });
    writeFileSync(
      codesPath,
      `/** Auto-generated by npm run data:generate — do not edit */\nexport const SUPPORTED_CPT_CODES = ${JSON.stringify(codeList)} as const;\n`,
    );
  }

  console.log(`Gzip assets ready: ${codeList.length} CPT codes`);
  return true;
}

async function main() {
  const mpfsPath = join(dataDir, 'mpfs-2026.json');
  const mpfsGzPath = join(dataDir, 'mpfs-2026.json.gz');
  const cptIndexPath = join(dataDir, 'cpt-index.json.gz');
  const ncciChunkMarker = join(dataDir, 'ncci-chunks', '8.json.gz');
  const ncciSeedPath = join(dataDir, 'ncci-seed.json');
  const codesPath = join(generatedDir, 'supported-cpt-codes.ts');
  const ifNeeded = process.argv.includes('--if-needed');

  if (ifNeeded) {
    const gzipOk = await ensureGzipFromExisting(dataDir, generatedDir, codesPath);
    if (gzipOk) return;
  }

  if (
    ifNeeded &&
    existsSync(mpfsPath) &&
    existsSync(mpfsGzPath) &&
    existsSync(cptIndexPath) &&
    existsSync(ncciChunkMarker) &&
    existsSync(ncciSeedPath) &&
    existsSync(codesPath)
  ) {
    console.log('Launch data present — skipping regeneration (--if-needed)');
    return;
  }

  const paths = cachePaths(CACHE_DIR);
  if (!existsSync(paths.pprrvu) || !existsSync(paths.zip) || !existsSync(ncciCacheDir(CACHE_DIR))) {
    console.log('CMS cache missing — fetching source files…');
    await fetchCmsSources();
  }

  const { conversionFactor, codes } = parsePprrvu(paths.pprrvu);
  const localities = parseGpci(paths.gpci);
  const zipMap = parseZipLocality(paths.zip);

  const mpfs = {
    version: '2026',
    source:
      'CMS Physician Fee Schedule RVU26B (April 2026, non-QPP) + CY2026 GPCI — educational benchmarks only',
    conversion_factor: conversionFactor,
    localities,
    codes,
  };

  writeFileSync(join(dataDir, 'mpfs-2026.json'), JSON.stringify(mpfs) + '\n');
  writeFileSync(join(dataDir, 'zip-to-locality.json'), JSON.stringify(zipMap) + '\n');

  const codeList = Object.keys(codes).sort();
  await writePricingGzipAssets(dataDir, mpfs, zipMap, codeList);

  console.log('Parsing NCCI practitioner PTP edits (this may take a minute)…');
  const ncciParsed = await parsePractitionerNcci(CACHE_DIR);

  /** Compact index keyed by column1 — rationale generated client-side. */
  const compactIndex = {};
  for (const [column1, edits] of Object.entries(ncciParsed.index)) {
    compactIndex[column1] = edits.map(({ column2, modifier }) => [column2, modifier]);
  }

  const chunksDir = join(dataDir, 'ncci-chunks');
  mkdirSync(chunksDir, { recursive: true });

  /** Split by HCPCS first character — browser loads only relevant slices (~20–70 KB each). */
  const byPrefix = {};
  for (const [column1, edits] of Object.entries(compactIndex)) {
    const prefix = column1[0].toUpperCase();
    if (!byPrefix[prefix]) byPrefix[prefix] = {};
    byPrefix[prefix][column1] = edits;
  }

  for (const [prefix, index] of Object.entries(byPrefix)) {
    await writeGzipJson(join(chunksDir, `${prefix}.json.gz`), {
      version: ncciParsed.version,
      prefix,
      format: 'compact-v1',
      index,
    });
  }

  /** Always-available fallback (~4 KB) for dev when chunks missing. */
  const seedIndex = {};
  for (const pair of NCCI_PTP_PAIRS) {
    if (!seedIndex[pair.column1]) seedIndex[pair.column1] = [];
    seedIndex[pair.column1].push([pair.column2, pair.modifier]);
  }
  writeFileSync(
    join(dataDir, 'ncci-seed.json'),
    `${JSON.stringify({ version: 'seed-v1', format: 'compact-v1', index: seedIndex })}\n`,
  );

  const pairCount = ncciParsed.pairs.length;
  writeManifest(dataDir, codeList, zipMap, ncciParsed, pairCount, Object.keys(byPrefix).length);

  mkdirSync(generatedDir, { recursive: true });
  writeFileSync(
    join(generatedDir, 'supported-cpt-codes.ts'),
    `/** Auto-generated by npm run data:generate — do not edit */\nexport const SUPPORTED_CPT_CODES = ${JSON.stringify(codeList)} as const;\n`,
  );

  console.log(
    `Generated ${codeList.length} HCPCS/CPT codes, ${Object.keys(localities).length} localities, ${Object.keys(zipMap).length} ZIP mappings, ${pairCount.toLocaleString()} NCCI pairs in ${Object.keys(byPrefix).length} browser chunks`,
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
